# Text colors

def init():
    global white
    global red
    global orange
    global yellow
    global green
    global blue
    global purple

    white = "\033[0;37m"
    red = "\033[1;31m"
    orange = "\033[1;33m"
    yellow = "\033[1;32m"
    green = "\033[1;36m"
    blue = "\033[1;34m"
    purple = "\033[1;35m"
